package BasicSA;
public class Array3 {
    public static void main(String [] args)
    {
        int a[][] ={{10,4,6},
    {10,56},
{10,56,7,89},
{1}};
for (int i=0; i<=3; i++)
{
    for (int j=0; j<a[i].length; j++)
    {
        System.out.print(a[i][j]+ " ");
    }
    System.out.println();
}
    }
    
}
